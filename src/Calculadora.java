public class Calculadora {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        
        int somarXeY = x + y;
        System.out.println("A soma é " + somarXeY);

        int subtrairXeY = x - y;
        System.out.println("A subtração é " + subtrairXeY);

        int multiplicarXeY = x * y;
        System.out.println("A multiplicação é " + multiplicarXeY);

        int dividirXeY = x / y;
        System.out.println("A divisão é " + dividirXeY);
    }
}
